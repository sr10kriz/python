def add(x, y):
    # print("the addition of X and Y ", x + y)
    return x + y


def sub(x, y):
    # print("the subtraction of X and Y ", x - y)
    return x - y


def multiply(x, y):
    # print("the multiplication of X and Y ", x * y)
    return x * y


def division(x, y):
    # print("the division of X and Y ", x / y)
    return x / y
